              child: Container(
                color: Theme.of(context).primaryColor.withOpacity(0.8),
              ),
            ),
            Container(
              width: double.infinity,
              height: 40,
              decoration: BoxDecoration(
                color: Theme.of(context).primaryColor.withOpacity(1),
                boxShadow: [
                  BoxShadow(
